import { Schema, model } from 'mongoose';

const pharmacySchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      min: 1,
      max: 5,
      required: true,
    },
  },
  {
    versionKey: false,
    timestamps: true,
  },
);

export const Pharmasy = model('Pharmasy', pharmacySchema);
